@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    body = request.get_json(silent=True)
    if not body or not body.get("text") or not body.get("creator_id"):
        return jsonify({"error": "Both 'text' and 'creator_id' are required."}), 400

    text = body["text"].strip()
    creator_id = body["creator_id"].strip()

    if len(text) < 10:
        return jsonify({"error": "Text is too short to analyze."}), 400

    llm_score, llm_reasoning = classify_with_llm(text)
    stylo_result = analyze_stylometrics(text)
    stylo_score = stylo_result["stylo_score"]
    word_count = stylo_result["word_count"]

    confidence = compute_confidence(llm_score, stylo_score, word_count)
    attribution = attribution_from_confidence(confidence)
    label = generate_label(confidence)
    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    write_log_entry({
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "text_snippet": text[:200],
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylo_score": stylo_score,
        "status": "classified",
        "label": label,
    })

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "llm_reasoning": llm_reasoning,
        "stylo_score": stylo_score,
        "stylo_detail": stylo_result,
        "label": label,
        "timestamp": timestamp,
    })


@app.route("/appeal", methods=["POST"])
def appeal():
    body = request.get_json(silent=True)
    if not body or not body.get("content_id") or not body.get("creator_reasoning"):
        return jsonify({"error": "Both 'content_id' and 'creator_reasoning' are required."}), 400

    content_id = body["content_id"].strip()
    reasoning = body["creator_reasoning"].strip()

    if not reasoning:
        return jsonify({"error": "'creator_reasoning' cannot be empty."}), 400

    entry = get_entry(content_id)
    if not entry:
        return jsonify({"error": f"No submission found with content_id '{content_id}'."}), 404

    if entry["status"] == "under_review":
        return jsonify({"error": "An appeal is already under review for this submission."}), 409

    ts = datetime.now(timezone.utc).isoformat()
    update_appeal(content_id, reasoning, ts)

    return jsonify({
        "status": "received",
        "content_id": content_id,
        "message": "Your appeal has been logged and will be reviewed.",
        "appeal_timestamp": ts,
    })


@app.route("/log", methods=["GET"])
def log():
    entries = get_log()
    return jsonify({"count": len(entries), "entries": entries})


