import uuid
import sqlite3
import json
from datetime import datetime, timezone

from flask import Flask, request, jsonify
from flask_limiter import Limiter
from flask_limiter.util import get_remote_address
from groq import Groq
from dotenv import load_dotenv

load_dotenv()

app = Flask(__name__)

limiter = Limiter(
    get_remote_address,
    app=app,
    default_limits=[],
    storage_uri="memory://",
)

groq_client = Groq(api_key=os.environ["GROQ_API_KEY"])

DB_PATH = "provenance.db"


# ---------------------------------------------------------------------------
# Database setup
# ---------------------------------------------------------------------------

def init_db():
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute("""
            CREATE TABLE IF NOT EXISTS audit_log (
                content_id      TEXT PRIMARY KEY,
                creator_id      TEXT NOT NULL,
                timestamp       TEXT NOT NULL,
                text_snippet    TEXT NOT NULL,
                attribution     TEXT NOT NULL,
                confidence      REAL NOT NULL,
                llm_score       REAL NOT NULL,
                stylo_score     REAL,
                status          TEXT NOT NULL DEFAULT 'classified',
                appeal_reasoning TEXT,
                appeal_timestamp TEXT,
                label           TEXT NOT NULL
            )
        """)
        conn.commit()


def write_log_entry(entry: dict):
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute("""
            INSERT INTO audit_log (
                content_id, creator_id, timestamp, text_snippet,
                attribution, confidence, llm_score, stylo_score,
                status, label
            ) VALUES (
                :content_id, :creator_id, :timestamp, :text_snippet,
                :attribution, :confidence, :llm_score, :stylo_score,
                :status, :label
            )
        """, entry)
        conn.commit()


def get_log(limit: int = 50) -> list:
    with sqlite3.connect(DB_PATH) as conn:
        conn.row_factory = sqlite3.Row
        rows = conn.execute(
            "SELECT * FROM audit_log ORDER BY timestamp DESC LIMIT ?", (limit,)
        ).fetchall()
    return [dict(r) for r in rows]


def get_entry(content_id: str) -> dict | None:
    with sqlite3.connect(DB_PATH) as conn:
        conn.row_factory = sqlite3.Row
        row = conn.execute(
            "SELECT * FROM audit_log WHERE content_id = ?", (content_id,)
        ).fetchone()
    return dict(row) if row else None


def update_appeal(content_id: str, reasoning: str, ts: str):
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute("""
            UPDATE audit_log
               SET status = 'under_review',
                   appeal_reasoning = ?,
                   appeal_timestamp = ?
             WHERE content_id = ?
        """, (reasoning, ts, content_id))
        conn.commit()


